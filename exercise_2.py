#Write a program that asks the user to enter two numbers and compares them using the following comparison operators: `>`, `<`, `>=`, `<=`, `==`, and `!=`. The program should print out the result of each comparison.
num1 = int(input("Enter the first number:"))
num2 = int(input("Enter the second number: "))

# carrying out the greater than operator
print(num1 > num2)

# carrying out the lesser than operator
print(num1 < num2)

# carrying out the greater than or equal to operator
print(num1 >= num2)

# carrying out the lesser than or equal to operator
print(num1 <= num2)

# carrying out the equal operator
print(num1 == num2)

# carrying out the not equal to operator
print(num1 != num2)